package com.example.Voting;

public class Candidates {

    private String can_name;
    private String can_address;
    private int can_phone;
    private  String can_description;
    private String can_nickname;

    public String getCan_name() {
        return can_name;
    }

    public void setCan_name(String can_name) {
        this.can_name = can_name;
    }

    public String getCan_address() {
        return can_address;
    }

    public void setCan_address(String can_address) {
        this.can_address = can_address;
    }

    public int getCan_phone() {
        return can_phone;
    }

    public void setCan_phone(int can_phone) {
        this.can_phone = can_phone;
    }

    public String getCan_description() {
        return can_description;
    }

    public void setCan_description(String can_description) {
        this.can_description = can_description;
    }

    public String getCan_nickname() {
        return can_nickname;
    }

    public void setCan_nickname(String can_nickname) {
        this.can_nickname = can_nickname;
    }
}
